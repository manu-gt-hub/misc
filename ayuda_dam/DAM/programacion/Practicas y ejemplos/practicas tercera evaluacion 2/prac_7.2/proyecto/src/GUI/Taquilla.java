    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       if(vendedora.getNum()>=turno)
       {
        String cad1=this.jTextField1.getText();
        String cad2=this.jTextField2.getText();
        int sala=0,cant=0;
        if(!cad1.isEmpty())
        {
            sala=Integer.parseInt(cad1);
            if(!cad2.isEmpty())
            {
               cant=Integer.parseInt(cad2);
               //si la cantidad no sobrepasa las entradas maximas
               if(cant<=ENTRADAS)
                {
                    //si la sala no sobrepasa el numero de salas del cine
                    if(sala<=SALAS)
                    {
                        cant=(cant*7);
                        this.jTextField3.setText(String.valueOf(cant));
                        ventas[Integer.parseInt(this.jTextField1.getText())-1]+=cant;
                        entradas[Integer.parseInt(this.jTextField1.getText())-1]+=Integer.parseInt(this.jTextField2.getText());
                    }
                    else
                    {
                        JOptionPane.showMessageDialog(rootPane,"No se puede elegir una sala mayor que: "+SALAS);
                    }
            
                }
                else
                {
                    JOptionPane.showMessageDialog(rootPane,"No se pueden comprar mas de "+ENTRADAS+" entradas");
                }
            }
            else
            {
                JOptionPane.showMessageDialog(rootPane,"Campos vacios");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Campos vacios");
        }  
       }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        /*si el numero por el que va la maquina es mayor que el turno de la taquilla
        significa que hay gente sin atender por lo tanto incremento el turno, de lo
        contrario solo reseteo los campos*/
        if(vendedora.getNum()>=turno)
        {
           turno++;
        }
        this.jTextField1.setText("");
        this.jTextField2.setText("");
        this.jTextField3.setText("");
        this.jTextField4.setText(String.valueOf(turno));
    }//GEN-LAST:event_jButton2ActionPerformed

