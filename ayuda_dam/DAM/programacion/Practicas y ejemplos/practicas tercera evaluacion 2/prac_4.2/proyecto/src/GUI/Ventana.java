    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       /*cada vez que se pulsa este boton se genera un numero aleatorio del 
        0 al 9 y se posiciona  en su casilla correspondiente hasta
        rellenar todos los digitos del boleto*/
        
       Random rand = new Random();
       int n = rand.nextInt(9);
       this.jTextField6.setText(String.valueOf(n));
       switch(cifra)
       {
           case 1:
                this.jTextField1.setText(String.valueOf(n));
           break;
           case 2:
               this.jTextField2.setText(String.valueOf(n));
           break;
           case 3:
               this.jTextField3.setText(String.valueOf(n));
           break;
           case 4:
               this.jTextField4.setText(String.valueOf(n));
           break;
           case 5:
               this.jTextField5.setText(String.valueOf(n));
           break;
       }
       cifra++;
       if(cifra>6)
       {
           this.dispose();
       }
    }//GEN-LAST:event_jButton1ActionPerformed


